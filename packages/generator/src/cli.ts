#!/usr/bin/env node
import { writeFile, mkdir } from 'node:fs/promises'
import path from 'node:path'
import { scanCorpus } from './scanner'
import { inferShape } from './infer'
import { emitInterface, pascalCase } from './emit-types'
import { emitGuardFunction } from './emit-guards'

/**
 * 引数を解釈する。`--corpus <dir> --out <parser/src/generated>`。
 *
 * @param argv - process.argv.slice(2)
 */
function parseArgs(argv: string[]): { corpus: string; out: string } {
  let corpus = ''
  let out = ''
  for (let i = 0; i < argv.length; i += 1) {
    if (argv[i] === '--corpus') corpus = argv[i + 1] ?? ''
    else if (argv[i] === '--out') out = argv[i + 1] ?? ''
  }
  if (!corpus || !out) {
    console.error('Usage: claude-code-jsonl-generator --corpus <dir> --out <parser/src/generated>')
    process.exit(2)
  }
  return { corpus, out }
}

async function main(): Promise<void> {
  const { corpus, out } = parseArgs(process.argv.slice(2))
  const scan = await scanCorpus(corpus)
  console.error(`scanned: ${scan.totalLines} lines, ${scan.samplesByType.size} types, ${scan.malformedLines} malformed`)

  const typeBlocks: string[] = []
  const guardBlocks: string[] = []
  const registry: string[] = []
  for (const [type, samples] of [...scan.samplesByType].toSorted()) {
    const shape = inferShape(samples)
    const name = `${pascalCase(type)}Entry`
    const fn = `is${pascalCase(type)}`
    typeBlocks.push(emitInterface(name, type, shape))
    guardBlocks.push(emitGuardFunction(fn, type, shape))
    registry.push(`  '${type}': ${fn},`)
  }

  const typesHeader = `/** GENERATED by claude-code-jsonl-generator. Do not edit by hand. */\n\n`
  const typesOut = typesHeader + typeBlocks.join('\n\n') + '\n'

  const guardsHeader =
    `/** GENERATED by claude-code-jsonl-generator. Do not edit by hand. */\n\n` +
    `function isObject(v: unknown): v is Record<string, unknown> {\n` +
    `  return typeof v === 'object' && v !== null && !Array.isArray(v)\n}\n` +
    `function isString(v: unknown): v is string { return typeof v === 'string' }\n` +
    `function isNumber(v: unknown): v is number { return typeof v === 'number' }\n` +
    `function isBoolean(v: unknown): v is boolean { return typeof v === 'boolean' }\n\n`
  const guardsRegistry =
    `\n\nexport const guards = {\n${registry.join('\n')}\n} as const satisfies Record<string, (v: unknown) => boolean>\n` +
    `export const KNOWN_TYPES = Object.keys(guards) as (keyof typeof guards)[]\n` +
    `export function guardByType(type: string, v: unknown): boolean | undefined {\n` +
    `  const g = (guards as Record<string, (x: unknown) => boolean>)[type]\n` +
    `  return g === undefined ? undefined : g(v)\n}\n`
  const guardsOut = guardsHeader + guardBlocks.join('\n\n') + guardsRegistry

  await mkdir(out, { recursive: true })
  await writeFile(path.join(out,'types.generated.ts'), typesOut, 'utf8')
  await writeFile(path.join(out,'guards.generated.ts'), guardsOut, 'utf8')
  console.error(`written: ${path.join(out,'types.generated.ts')}, ${path.join(out,'guards.generated.ts')}`)
  console.error('NOTE: review the diff before committing. content-block guards / message shapes are curated manually (see plan Task 06/08).')
}

main().catch((error: unknown) => {
  console.error(error)
  process.exit(1)
})
